private ModificacionBajaInmueble obtenerModificarFrame() {
        JInternalFrame[] frames = getDesktopPane().getAllFrames();
        for (JInternalFrame frame : frames) {
            if (frame instanceof ModificacionBajaInmueble) {
                return (ModificacionBajaInmueble) frame;
            }
        }
        return null; // Devuelve null si no se encuentra el frame "AltaClientes"
    }

