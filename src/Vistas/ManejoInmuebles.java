    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
    Inmueble in = new Inmueble();
    InmuebleData inD = new InmuebleData();
        if (evt.getClickCount() == 2) {
            int fila = jTable1.getSelectedRow(); // Obtiene la fila seleccionada al hacer doble clic
            if (fila >= 0) {
                ModificacionBajaInmueble mbi= new ModificacionBajaInmueble(); //Creo un objeto del internalFrame
                Menu.Escritorio.add(mbi);//llamo al escritorio del menu y agrego el internal
                mbi.toFront();//con este metodo hago que el internal se muestre delante del internal que lo llama
                mbi.setVisible(true);//metodo para mostrar el internal 
                this.setVisible(false);
            }
            if (fila != -1) {
                int idInmueble = (int) jTable1.getValueAt(fila, 0); // creo una variable y e asigno el valor a transferir
                in = inD.buscarPropiedadId(idInmueble);
                }
                

                // Accede al JInternalFrame " ModificarCliente" y llama al método para establecer el valor en el JTextField
                ModificacionBajaInmueble clienteF = obtenerModificarFrame();//metodo que hace referncia a  ModificarCliente
                 
                if (clienteF != null) {
                    clienteF.setTextFieldValue(in);//Paso por parametros los datos que va a recibir  ModificarCliente
                }
            }
        
    }//GEN-LAST:event_jTable1MouseClicked


