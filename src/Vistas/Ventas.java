    private PagoVenta obtenerVentasFrame() {
        JInternalFrame[] frames = getDesktopPane().getAllFrames();
        for (JInternalFrame frame : frames) {
            if (frame instanceof PagoVenta) {
                return (PagoVenta) frame;
            }
        }
        return null; // Devuelve null si no se encuentra el frame "AltaClientes"
    }

    public void agregarDatoAComboBoxPago(Venta dato) {
        PagoVenta pago = obtenerVentasFrame(); // Obtén la referencia al JInternalFrame "AltaClientes"
        if (pago != null) {
            pago.agregarElementoAComboBox(dato); // Agrega el dato al JComboBox "jcombo2" en "AltaClientes"
        }
    }

    public void llenarInmueble() {
        jInmueble.addItem(null);
        InmuebleData inData = new InmuebleData();
        for (Inmueble i : inData.buscarPropiedadesAlqVta("venta", true)) {
            jInmueble.addItem(i);
            System.out.println(i.getDireccion()+" - "+i.getProp());
        }
    }

    private void llenarCliente() {
        jCliente.addItem(null);
        ClienteData cData = new ClienteData();
        for (Cliente c : cData.listarTodosClientesActivos()) {
            jCliente.addItem(c);
        }

    }

    private void llenarEscribano() {
        jEscribano.addItem(null);
        EscribanoData eData = new EscribanoData();
        for (Escribano e : eData.listarEscribanos()) {
            jEscribano.addItem(e);
        }
    }

