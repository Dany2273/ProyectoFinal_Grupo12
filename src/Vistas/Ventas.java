    private void jClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jClienteActionPerformed
        // TODO add your handling code here:

        if (jCliente.getSelectedItem()!= null) {
            Cliente c = (Cliente) jCliente.getSelectedItem();

            if (c.getTipo() == TipoCliente.PERSONA_FISICA) {
                jConyugue.setEnabled(true);
                jConyugue.addItem(c.getConyugue());
            } else if (c.getTipo() == TipoCliente.PERSONA_JURIDICA) {
                jConyugue.setSelectedItem(null);
                jConyugue.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jClienteActionPerformed

