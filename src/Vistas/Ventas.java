    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        if (jdFecha.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la Fecha de Venta!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (jInmueble.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la propiedad en Venta!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (jCliente.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar el Cliente!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (jEscribano.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar el nombre del Escribano!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (jTVenta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Falta ingresar el precio de venta!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }  else if (jcescritura.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar el estado de la Escriturización!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (jTDetalles.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar los detalles de la venta!!.", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!jTVenta.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "Ingrese un importe válido! (solo números).",
                    "Error en el DNI", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Venta ven = null;
        VentaData vData = new VentaData();
        Conyugue con;
        Escribano esc;
        Cliente cli;
        Inmueble inm;
        Propietario prop;
        String mon = null;

        LocalDate fecha = jdFecha.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        inm = (Inmueble) jInmueble.getSelectedItem();
        prop = inm.getProp();
        cli = (Cliente) jCliente.getSelectedItem();
        con = (Conyugue) jConyugue.getSelectedItem();
        esc = (Escribano) jEscribano.getSelectedItem();
        double pVenta = Double.parseDouble(jTVenta.getText());
        if (jrPes.isSelected()) {
            mon = "Pesos";
        } else if (jrDol.isSelected()) {
            mon = "Dolares";
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar el tipo de moneda.");
        }
        String escritura = (String) jcescritura.getSelectedItem();
        String det = jTDetalles.getText();
        int resp = JOptionPane.showConfirmDialog(null, "¿Desea guardar los datos ingresados?", "Atención!",
                JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (resp == 0) {
            ven = new Venta(fecha, inm, prop, cli, con, esc, pVenta, mon, det, escritura);

            vData.agregarVenta(ven);

            pagoVentaFrame = new PagoVenta();
            Menu.Escritorio.add(pagoVentaFrame);
            
             int x = (getWidth() - pagoVentaFrame.getWidth()) / 3;
        int y = (getHeight() - pagoVentaFrame.getHeight()) / 3;
        pagoVentaFrame.setLocation(x, y);
        pagoVentaFrame.toFront();
            pagoVentaFrame.setVisible(true);
            agregarDatoAComboBoxPago(ven);
            dispose();
        }

    }//GEN-LAST:event_jBGuardarActionPerformed

