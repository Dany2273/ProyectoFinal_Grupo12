    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        ListaVentas listaVentas = obtenerListasVentasFrame();

        if (listaVentas != null) {
            DefaultTableModel modelo = listaVentas.modelo; // Suponiendo que tiene un método getTableModel() en DatosClientes
            int fila = listaVentas.getSelectedRow(); // Suponiendo que tiene un método para obtener la fila seleccionada en DatosClientes
            if (fila != -1) {
                int valor = (int) modelo.getValueAt(fila, 0); // Obtener el valor como Long
//                long cuil = valor.longValue();
                Venta venta = vData.buscarVenta(valor);
                LocalDate nuevaF = jdFecha.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
                Inmueble nuevoI = (Inmueble) jInmueble.getSelectedItem();
                Propietario nuevoP = (Propietario) jpropietario.getSelectedItem();
                Cliente nuevoC = (Cliente) jCliente.getSelectedItem();
                Conyugue nuevoCo = (Conyugue) jConyugue.getSelectedItem();
                Escribano nuevoE = (Escribano) jEscribano.getSelectedItem();
                double nuevoPr = Double.parseDouble(jprecio.getText());
                String nuevaM = null ;
                 if (jrPes.isSelected()) {
            nuevaM = "Pesos";
        } else if (jrDol.isSelected()) {
            nuevaM = "Dolares";
        }
                 String nuevoD = jTDetalles.getText();
                 String nuevaEsc = (String) jcescritura.getSelectedItem();
                 
                 venta.setFecha(nuevaF);
                 venta.setInmueble(nuevoI);
                 venta.setPropietario(nuevoP);
                 venta.setCliente(nuevoC);
                 venta.setConyugue(nuevoCo);
                 venta.setEscribano(nuevoE);
                 venta.setPrecioVenta(nuevoPr);
                 venta.setMoneda(nuevaM);
                 venta.setEstadoEscriturizacion(nuevaEsc);
                 venta.setDetallesVenta(nuevoD);
                 
               

                vData.modificarVenta(venta);

                // Actualiza los datos en el modelo de la tabla
                if (fila != -1) {
                    String  cony;
                    
                    modelo.setValueAt(venta.getIdVenta(), fila, 0); // Actualiza el id
                    modelo.setValueAt(venta.getFecha(), fila, 1); // Actualiza fecha
                     modelo.setValueAt(venta.getInmueble(), fila, 2); // Actualiza inmueble
                      modelo.setValueAt(venta.getPropietario(), fila, 3); // Actualiza propietario
                       modelo.setValueAt(venta.getCliente(), fila, 4); // Actualiza cliente
                    if (venta.getConyugue() == null) {
                        cony = "N/A.";
                        modelo.setValueAt(cony, fila, 5); // Actualiza conyugue si es nulo
                    } else {
                        modelo.setValueAt(venta.getConyugue(), fila, 5); // Actualiza conyugue
                    }

                   
                   
                    modelo.setValueAt(venta.getEscribano(), fila, 6); // Actualiza escribano
                    modelo.setValueAt(venta.getPrecioVenta(), fila, 7); // Actualiza el valor de la venta
                    modelo.setValueAt(venta.getMoneda(), fila, 8); // Actualiza tipo de moneda
                    modelo.setValueAt(venta.getEstadoEscriturizacion(), fila, 9); // Actualiza estado de escritura
                    modelo.setValueAt(venta.getDetallesVenta(), fila, 10);
                }

//// Notifica a la tabla que los datos han cambiado
                modelo.fireTableDataChanged();
                dispose();
            }
        }


    }//GEN-LAST:event_jBGuardarActionPerformed

